def run_benchmark():
    print("🚀 Başlatılıyor: Maxwell Engine Güvenlik Zafiyeti Benchmark'ı V2")
    print("----------------------------------------------------------------")
    
    model_name = "Qwen/Qwen2.5-1.5B-Instruct-GGUF:qwen2.5-1.5b-instruct-q4_k_m.gguf"
    print(f"[*] Model indiriliyor veya kontrol ediliyor: {model_name}")
    try:
        model_path = download_model_if_needed(model_name)
    except Exception as e:
        print(f"[!] Model yüklenirken hata oluştu: {e}")
        sys.exit(1)
        
    print("[*] Termodinamik Motor (LLAMA-CPP) başlatılıyor...\n")
    engine = MaxwellEngine(model_path=model_path)
    
    results = []
    
    # Konsol çıktısı formatı
    print(f"| {'Test Senaryosu':<35} | {'Raw':<8} | {'Global':<8} | {'Peak':<8} | {'Diverge':<8} | {'Label':<10} | {'Status':<15} |")
    print("-" * 110)
    
    for test_name, test_data in TEST_PAYLOADS.items():
        payload = test_data["code"]
        label = test_data["label"]
        try:
            # Sadece matematiksel hesaplamalar çalıştırılıyor
            surp_data = engine.inference.calculate_surprisal(payload)
            fractal_data = engine.inference.calculate_fractal_surprisal(payload)
            
            raw = surp_data["raw_surprisal"]
            glbl = fractal_data["raw_global_surprisal"]
            peak = fractal_data["raw_peak_surprisal"]
            diverge = fractal_data["local_global_divergence"]
            
            # Risk sınıflandırmasını şimdilik "Surprisal" seviyesi olarak yapıyoruz, Security etiketi değil.
            if peak >= 7.0:
                status = "High Surp."
            elif peak >= 4.0:
                status = "Med Surp."
            else:
                status = "Low Surp."
                
            print(f"| {test_name:<35} | {raw:<8.4f} | {glbl:<8.4f} | {peak:<8.4f} | {diverge:<8.4f} | {label:<10} | {status:<15} |")
            
            results.append({
                "test": test_name,
                "raw": raw,
                "global": glbl,
                "peak": peak,
                "divergence": diverge,
                "label": label,
                "status": status
            })
            
        except Exception as e:
            print(f"| {test_name:<35} | HATA: {str(e)}")
            results.append({"test": test_name, "raw": "HATA", "global": "HATA", "peak": "HATA", "divergence": "HATA", "label": label, "status": str(e)})
            
    print("\n[*] Benchmark başarıyla tamamlandı.")
    
    # Save results to a markdown file
    os.makedirs("benchmarks", exist_ok=True)
    timestamp = datetime.datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
    filepath = os.path.join("benchmarks", f"benchmark_results_{timestamp}.md")
    
    with open(filepath, "w", encoding="utf-8") as f:
        f.write("# Maxwell Engine Security Benchmark V2\n\n")
        
        # Metadata
        f.write("## Metadata\n")
        f.write("```json\n")
        metadata = {
            "maxwell_version": "0.2.0",
            "date": datetime.datetime.now().isoformat(),
            "model": model_name,
            "quantization": "Q4_K_M",
            "normalizer": "legacy_fixed",
            "router": "fixed_window",
            "chunk_size": 64,
            "stride": 64
        }
        f.write(json.dumps(metadata, indent=2))
        f.write("\n```\n\n")
        
        f.write("## Test Results (Raw Mathematical Surprisal)\n\n")
        f.write("| Test Senaryosu | Raw Surprisal | Global Surprisal | Peak Surprisal | Divergence | Ground Truth Label | Engine Status |\n")
        f.write("| --- | --- | --- | --- | --- | --- | --- |\n")
        for r in results:
            raw_str = f"{r['raw']:.4f}" if isinstance(r['raw'], float) else r['raw']
            glbl_str = f"{r['global']:.4f}" if isinstance(r['global'], float) else r['global']
            peak_str = f"{r['peak']:.4f}" if isinstance(r['peak'], float) else r['peak']
            div_str = f"{r['divergence']:.4f}" if isinstance(r['divergence'], float) else r['divergence']
            
            f.write(f"| {r['test']} | {raw_str} | {glbl_str} | {peak_str} | {div_str} | {r['label']} | {r['status']} |\n")
            
    print(f"[*] Rapor kaydedildi: {filepath}")

if __name__ == "__main__":
    run_benchmark()
